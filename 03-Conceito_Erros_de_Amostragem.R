# Revisando que são Parâmetros e Estatísticas

# -> Parâmetros:   valores que descrevem características da população, como média e mediana da população.
#                  ou seja, valores numéricos que descrevem características específicas da população como um todo.

# -> Estatísticas: valores calculados a partir da amostra, como média e mediana da amostra.
#                  ou seja, valores calculados a partir de uma amostra retirada da população.


# - Como as Estatísticas são calculadas a partir da amostra, que é uma parte da população, não seria razoável esperar que a média dos dados da
#   amostra fosse igual a média dos dados da população ?

# - Seria então esperado que ambas as médias fossem iguais caso não houvessem erros.

# - A diferença entre estes 2 valores é chamada de "erro de amostragem da média da amostra".

# - E por que esses erros ocorreriam? 
#   -> Porque a amostragem não foi feita de maneira adequada.
#   -> Porque a amostra não é representativa da população.
#   -> Porque não temos uma amostra com tamanho suficiente para fazer inferência sobre a população.
#   -> Entro outros...

# - Quanto maior o tamanho da amostra, menor será o erro da amostragem.




